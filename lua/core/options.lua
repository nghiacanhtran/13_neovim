-- settings.lua

-- Enable relative line numbers
vim.opt.relativenumber = true

-- Set the tab width
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true

-- Enable mouse support
vim.opt.mouse = 'a'

-- Enable syntax highlighting
vim.cmd('syntax on')

-- Set leader key
vim.g.mapleader = " "
